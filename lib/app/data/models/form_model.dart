import 'package:formularios_front/app/domain/entities/form_entity.dart';
import 'package:formularios_front/app/domain/entities/information_field_entity.dart';
import 'package:formularios_front/app/domain/entities/section_entity.dart';
import 'package:formularios_front/app/domain/enum/form_status_enum.dart';
import 'package:formularios_front/app/domain/enum/priority_enum.dart';

class FormModel extends FormEntity {
  FormModel({
    required super.externFormId,
    required super.internFormId,
    required super.creatorUserId,
    required super.userId,
    required super.coordinatorsId,
    required super.template,
    required super.area,
    required super.system,
    required super.street,
    required super.city,
    required super.number,
    required super.latitude,
    required super.longitude,
    required super.region,
    required super.priority,
    required super.status,
    required super.expirationDate,
    required super.creationDate,
    required super.sections,
    super.comments,
    super.description,
    super.endDate,
    super.informationFields,
    super.justificative,
    super.startDate,
    super.vinculationFormId,
  });

  FormModel copyWith({
    String? externFormId,
    String? internFormId,
    String? creatorUserId,
    String? userId,
    List<String>? coordinatorsId,
    String? vinculationFormId,
    String? template,
    String? area,
    String? system,
    String? street,
    String? city,
    int? number,
    double? latitude,
    double? longitude,
    String? region,
    String? description,
    PriorityEnum? priority,
    FormStatusEnum? status,
    int? expirationDate,
    int? creationDate,
    int? startDate,
    int? endDate,
    String? justificative,
    String? comments,
    List<SectionEntity>? sections,
    List<InformationFieldEntity>? informationFields,
  }) {
    return FormModel(
      externFormId: externFormId ?? this.externFormId,
      internFormId: internFormId ?? this.internFormId,
      creatorUserId: creatorUserId ?? this.creatorUserId,
      userId: userId ?? this.userId,
      coordinatorsId: coordinatorsId ?? this.coordinatorsId,
      vinculationFormId: vinculationFormId ?? this.vinculationFormId,
      template: template ?? this.template,
      area: area ?? this.area,
      system: system ?? this.system,
      street: street ?? this.street,
      city: city ?? this.city,
      number: number ?? this.number,
      latitude: latitude ?? this.latitude,
      longitude: longitude ?? this.longitude,
      region: region ?? this.region,
      description: description ?? this.description,
      priority: priority ?? this.priority,
      status: status ?? this.status,
      expirationDate: expirationDate ?? this.expirationDate,
      creationDate: creationDate ?? this.creationDate,
      startDate: startDate ?? this.startDate,
      endDate: endDate ?? this.endDate,
      justificative: justificative ?? this.justificative,
      comments: comments ?? this.comments,
      sections: sections ?? this.sections,
      informationFields: informationFields ?? this.informationFields,
    );
  }
}
